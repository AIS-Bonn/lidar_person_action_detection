        self.start_iter = 0
        self.max_iter = cfg.SOLVER.MAX_ITER
        self.cfg = cfg

        # Assume these objects must be constructed in this order.
        model = self.build_model(cfg)

        if MaskDINO_weights:
            self.checkpointer = DetectionCheckpointer(
                # Assume you want to save checkpoints together with logs/statistics
                model,
                cfg.OUTPUT_DIR,
                **kwargs,
            )
            self.resume_or_load(resume=resume)

        if cfg.MODEL.BACKBONE.NAME == "D2SwinTransformer":
            net = lidar(channels = len(cfg.MODEL.CHANNELS), encoding = cfg.MODEL.ENCODING).cuda()
            model.backbone.patch_embed = torch.nn.Sequential(net, model.backbone.patch_embed)

