class lidar(torch.nn.Module):
    def __init__(self, channels = 4, encoding = False):
        super(lidar, self).__init__()
        if encoding:
            channels = channels + 3
        self.encode = torch.nn.Conv2d(channels, 3, 3, padding='same')
    
    def forward(self, x):
        #print(x.shape)
        y = self.encode(x)
        return y
